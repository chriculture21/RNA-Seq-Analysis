# Functional enrichment analysis
# If not installed, install biomart package

# BiocManager::install("biomaRt")
# Load the library
library('biomaRt')

# Create the mart object
mart <- useEnsemblGenomes(
  biomart = "plants_mart",
  dataset = "vvinifera_eg_gene", # Choose the desired genome, for example here we use the Vitis vinifera dataset
  host = "https://plants.ensembl.org"
)

listDatasets(mart) # You can list the available datasets with this fxn

attributes <- listAttributes(mart) # This lists the attributes that you may need to annotate your genes

# Search for annotation based on ensembl_gene_id identifiers
annot <- getBM(filters= "ensembl_gene_id", # Which identifier you are using
               attributes= c("ensembl_gene_id", 
                             "description", 
                             "start_position", 
                             "end_position", 
                             "strand", 
                             "external_gene_name",
                             "name_1006",
                             "go_id"), # Which attributes you want to collect
               values=rownames(res_df), # The code names of your genes
               mart= mart) # The mart you previously defined 

# Save an RDS file
# Some times the server is down, so you save the RDS in order to be able to work offline
saveRDS(annot, "Zm_annotation_full.rds")
annot <- readRDS("Zm_annotation_full.rds") # Read the RDS file

# Join the results table with the annotations
res_df$ensembl_gene_id <- rownames(res_df)

res_df_annot<- res_df %>%
  left_join(annot, by = 'ensembl_gene_id')

#Save the data as an xlsx file
write.xlsx(res_df_annot, file="choose/your/path/Annotated table for DEGs.xlsx", colNames=TRUE, rowNames=TRUE)

#------------------------------------------------------------------------------------------------------------
# Let's start now with the annotation of our genes. In not installed, install clusterProfiler package
#BiocManager::install("clusterProfiler")
# Load the clusterProfiler library
library(clusterProfiler)

## Start with the clusterProfiler package for gene ontology (GO) analysis
go_map <- getBM(filters= "ensembl_gene_id",
  attributes = c(
    "ensembl_gene_id",
    "go_id",
    "name_1006",
    "namespace_1003"),
  mart = mart,
  values=rownames(res_df)
)

go_map <- go_map[!is.na(go_map$go_id), ] #remove NAs

# Create the dataframes with the GO IDs and the GO names
TERM2GENE <- go_map[, c("go_id", "ensembl_gene_id")]
TERM2NAME <- unique(go_map[, c("go_id", "name_1006")])

# Filter only for the significant genes
background_genes <- rownames(res_df) # It is very important to have your the background genes, which are critical for the statistical context
sig_genes <- rownames(res_df)[res_df$padj < 0.05] # Change the adjusted p-value to your desire

# Create the enrichment object
ego <- enricher(
  gene = sig_genes,
  TERM2GENE = TERM2GENE,
  TERM2NAME = TERM2NAME,
  universe = background_genes,
  pAdjustMethod = "BH",
  minGSSize = 10,
  maxGSSize = 500
)

# Add ontology (namespace_1003) to the enrichment results
ego@result <- merge(
  ego@result,
  unique(go_map[, c("go_id", "namespace_1003")]),
  by.x = "ID",
  by.y = "go_id",
  all.x = TRUE
)

# Add 
ego@result$Ontology <- dplyr::recode(
  ego@result$namespace_1003,
  "biological_process" = "Biological_Process",
  "molecular_function" = "Molecular_Function",
  "cellular_component" = "Cellular_Component"
)

ego@result$Description <- stringr::str_wrap(ego@result$Description, width = 100)

# Save the plot
pdf('choose/your/path/Dotplot.pdf',
    width = 10,
    height = 7)

dotplot(ego, color = "p.adjust",
        showCategory = 30,
        label_format = 100)+
  aes(x = FoldEnrichment,
      y = Description)+
  labs(
    x = "Fold Enrichment",
    y = "GO Term Description")+
  ggtitle('GO Enrichment')+
  geom_point(
    aes(
      size = Count,
      fill = p.adjust,
      color = Ontology
    ),
    shape = 21, stroke = 1.2
  ) +
  scale_color_manual(
    values = c(
      Biological_Process = "black",
      Molecular_Function = "darkgreen",
      Cellular_Component = "darkblue"
    ))+
  theme(
    plot.title = element_text(size = 14, face = "bold"))

dev.off() # Turn off the device

