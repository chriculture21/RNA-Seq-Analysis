# Load required libraries
library(DESeq2)
library(tidyverse)
library(dplyr)
library(EnhancedVolcano)
library(pheatmap)
library(apeglm)
library(RColorBrewer)
library(ggplot2)
library(openxlsx)

# Set your working directory
setwd('choose/your/path')

# Read Metadata file containing the sample information
metadata <- read.delim("metadata.txt")

# Import the gene raw counts generated by featureCounts (see 4.featureCounts.sh file)
counts <- read.table("mod_countsFV.txt", header = TRUE, row.names = 1)
rownames(counts) <- sub("^gene:", "", rownames(counts)) # remove prefixes if needed. Substitute the ^gene: with whatever the prefix is.

# Check if the metadata rownames are the same as the counts table column names
metadata_samples <- metadata$Sample_Name
all(metadata_samples == colnames(counts))
rm(metadata_samples) #after checking, remove the metadata_samples

# Indicate the desired factor (Condition, time_point, etc) and generate the DESeq object.
metadata$Condition <- factor(metadata$your_factor) #change the factor to your desire (for example "Condition")
dds = DESeqDataSetFromMatrix(countData =  counts, 
                             colData =  metadata, 
                             design = ~ your_factor) #Construct the DESeq object.

# Pre-filter lowly expressed genes. Here, we determine the lowest number of observations in all samples as 10, present in at least three samples.
keep <- rowSums(counts(dds) >= 10) >= 3
dds <- dds[keep,]

#---------------------------------------------------------------------------------------------------
# Quality control and exploratory data analysis
# Principal Component Analysis (PCA) - PCA helps to understand the structure and the variability of your data and if your samples are grouped as expected
vsdata <- vst(dds, blind = TRUE) # Estimate dispersion and apply a variance stabilizing transformation
# using the DESEQ2 plotPCA fxn we can do a principal component analysis
plotPCA(vsdata, intgroup = 'your_factor')+
  ggtitle("Principal Component Analysis")+
  geom_point(size = 6) 

#Check sample distances and create a heatmap to check sample correlations
sampleDists <- dist(t(assay(vsdata)))
pheatmap(
  as.matrix(sampleDists),
  clustering_distance_rows = sampleDists,
  clustering_distance_cols = sampleDists
)

# Determine your reference group to compare your samples against. It could be Mock, Healthy or anything depending on your metadata
dds$your_factor <- relevel(dds$your_factor, ref = "your_reference")

# Run the deferential expression analysis using DESeq
dds <- DESeq(dds, minReplicatesForReplace = Inf)
resultsNames(dds)
res <- results(dds, contrast = c("your_factor", "treatment1", "your_reference")) # Change the treatment group to be compared with the reference group according to your desire
summary(res)

# Remove NAs and then sort summary list by the false discovery rate (FDR) adjusted p-value 
res <- res[!is.na(res$padj), ]
res <- res[order(res$padj), ]
head(res)

# Shrink data to reduce the noise from the lowly-expressed genes and create a diagnostic MA plot
resLFC <- lfcShrink(
  dds,
  coef = "your_factor_treatment1_vs_your_reference",
  type = "apeglm",
) # This command shrinks the noisy gene expression data, and it is useful if you have low number of samples or lowly expressed genes

summary(resLFC) # Check the differences with the summary(res)

#Create the MA plot
pdf('choose/your/path/MA Plot.pdf', #open the pdf device to create a high-quality publication plot
    width = 7,
    height = 5)

plotMA(resLFC, ylim = c(-5, 5), main = "MA Plot")

dev.off() # Close the devise and save your pdf file with the plot

#Create a data frame to extract the results from the results object, with shrunk data
res_df <- as.data.frame(resLFC)

#Add a column to determine if a gene is up- or down- regulated if the fold change is above or below 1. 
#Start with p-adj = 0.05 as your cutoff and then you can play with the settings
res_df <- res_df %>%
  mutate(Significance = case_when(
    log2FoldChange >=1 & padj <= 0.05 ~ 'Upregulated',
    log2FoldChange <=-1 & padj <= 0.05 ~ 'Downregulated',
    abs(log2FoldChange) < 1 | padj > 0.05 ~ 'Not significant'
  ))

res_df <- res_df[!is.na(res_df$padj), ] #remove NAs

# Reorder the table so that the smallest FDR adjusted p-value is on the top of the data frame
res_df=res_df[order(res_df$padj),]

# Save the table as an xlsx file
write.xlsx(res_df, 'choose/your/path/your_excel_file.xlsx', colNames=TRUE, rowNames=TRUE)

#-------------------------------------------------------------------------------------------
# Continue with the exploratory analysis
top_genes <- res_df %>%
  filter(padj <= 0.05, abs(log2FoldChange) >= 1) %>%
  arrange(padj) %>%
  head(10)

# Create the volcano plot to check patterns on gene expression
pdf('choose/your/path/Volcano plot.pdf',
    width = 7,
    height = 5)

ggplot(data=res_df, 
       aes(x=log2FoldChange, y=-log10(padj), color = Significance)) + 
  geom_point() + 
  geom_vline(xintercept=c(-1, 1), col="red", linetype = 'dashed') + # here you can change the log fold change to your desire
  geom_hline(yintercept=-log10(0.05), col="red", linetype = 'dashed') + # here you can change the adjusted p-value to your desire
  theme_gray()+
  geom_text_repel(
    data = top_genes,
    aes(label = rownames(top_genes)), # you annotate the code of your top expressed genes into the plot
    color = 'black'
  ) +
  labs(x = 'LogFC', y = '-Log10(FDR)') + 
  scale_color_manual(values = c('blue','grey70','red'))+ #blue for down-regulation, grey for not-significant and red for the up-regulation
  labs(
    title = 'your title',
    subtitle = 'Log2FC cutoff = 1; Adjusted p-value cutoff = 0.05' # Change the subtitle according to your desire
  )+ theme(
    plot.title = element_text(size = 14, face = "bold"),
    plot.subtitle = element_text(size = 11, face = "italic")
  )

dev.off() # Close the devise and save the pdf with your plot
